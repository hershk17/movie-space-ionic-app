CREATE TABLE IF NOT EXISTS moviesTable(
    id INTEGER PRIMARY KEY,
    title TEXT,
    posterURL TEXT,
    overview TEXT,
    adult INTEGER,
    backdropURL TEXT,
    genres TEXT,
    homepage TEXT,
    lang TEXT,
    popularity INTEGER,
    releaseDate TEXT,
    revenue INTEGER,
    runtime INTEGER,
    tagline TEXT,
    video INTEGER,
    voteAvg INTEGER,
    voteCnt INTEGER,
    userWatchStatus TEXT,
    userRating INTEGER
);